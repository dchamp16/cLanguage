#include <iostream>
using namespace std;

int main()
{
    /* can be use ascii code  when using char data*/
    char a = 80, b = 101, c = 116, d = 101, e = 114; // variable = (use decimal) not the hex
    cout << a;                                       // output: P
    cout << b;                                       // output: e
    cout << c;                                       // output: t
    cout << d;                                       // output: e
    cout << e;                                       // output: r
    return 0;
}