#include <iostream>
using namespace std;

int main()
{
    int age; // initialize age for storing integer coming from the cin

    /* cout -> see-out: used to output and uses the insection operator (<<) */
    cout << "Enter age: "; // output Enter age

    /* cin -> see-in: used for input uses the extration o */
    cin >> age; // command to input number to age

    cout << "The entered age is: " << age << endl; // output The entered age is: age
    return 0;
}